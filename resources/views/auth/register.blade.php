    <script>
        function togglePasswordVisibility(fieldId, iconId) {
            const passwordField = document.getElementById(fieldId);
            const toggleIcon = document.getElementById(iconId);

            if (passwordField.type === 'password') {
                passwordField.type = 'text';
                // Change icon to 'eye-slash' (closed eye)
                toggleIcon.setAttribute('d', 'M13.875 18.825A10.05 10.05 0 0112 19c-4.478 0-8.268-2.943-9.542-7 1.274-4.057 5.064-7 9.542-7a10.05 10.05 0 011.825.125M16 12a4 4 0 11-8 0 4 4 0 018 0zM17.5 6.5l-4.5 4.5'); // Path for eye with slash
            } else {
                passwordField.type = 'password';
                // Change icon to 'eye' (open eye)
                toggleIcon.setAttribute('d', 'M15 12a3 3 0 11-6 0 3 3 0 016 0zM2.458 12C3.732 7.943 7.523 5 12 5c4.478 0 8.268 2.943 9.542 7-1.274 4.057-5.064 7-9.542 7-4.477 0-8.268-2.943-9.542-7z'); // Default eye path
            }
        }

        function showPasswordRequirements() {
            document.getElementById('passwordRequirements').classList.remove('hidden');
        }

        function hidePasswordRequirements() {
            document.getElementById('passwordRequirements').classList.add('hidden');
        }
    </script>
